	function index(){
		Configure::write('debug', 1);
		$this->LegacyRegionsCategory->recursive = 2;
		$this->autoRender = false;
		$items = $this->LegacyRegionsCategory->findAll(null, null, null, 0);
		$count = 0;
		foreach($items as $item){
			$region_alias = @$this->Alias->find('Alias.src = "taxonomy/term/'.$item['LegacyRegion']['TermData']['tid'].'"');
			$category_alias = @$this->Alias->find('Alias.src = "taxonomy/term/'.$item['LegacyCategory']['TermData']['tid'].'"');
			$items[$count]['region_alias'] = $region_alias;
			$items[$count]['category_alias'] = $category_alias;
			$output[$count]['legacy_url'] = $item['LegacyRegionsCategory']['legacy_url'];
/* 			$cat_term = $items[$count]['category_alias']['Alias']['dst']; */
			$cat_term = str_ireplace('taxonomy/term/', '', $items[$count]['category_alias']['Alias']['src']);
			if(empty($cat_term)){
				$cat_term = null;
			}
/* 			$region_term =  $items[$count]['region_alias']['Alias']['dst']; */
			$region_term = str_ireplace('taxonomy/term/', '', $items[$count]['region_alias']['Alias']['src']);
			if(empty($region_term)){
				$region_term = null;
			}
			$output[$count]['category_term'] = $cat_term;
			$output[$count]['region_term'] = $region_term;
			$count++;
		}
		foreach($output as $value){
			if(!empty($value['category_term']) && !empty($value['region_term'])){
				$urls[$value['legacy_url']] = 'taxonomy/term/'.$value['region_term'].'/'.$value['category_term'];
			}
		}
		
		ksort($urls);
		$now = time();
		$output = null;
		foreach($urls as $key => $value){
			$output.= "INSERT INTO `drupal_path_redirect` (`rid`, `source`, `redirect`, `query`, `fragment`, `language`, `type`, `last_used`) VALUES ('', '$key', '$value', '', '', '', 301, $now);\r";
		}
	//	echo "<pre>$output</pre>";
		debug($urls);
		echo '<ol>';
		foreach($urls as $key => $value){
			echo "<li><font color='darkred'>$key</font>  <font color='darkcyan'>$value</font></li>";
		}
		echo '</ol>';
		debug($output);
	//	debug($items);
	}
	
	

